namespace Primeiro.Model
{
    public class Contas
    {
        public int Somar(int a, int b)
        {
            return a + b;
        }

        public int Substrair(int a, int b)
        {
            return a - b;
        }

        public int Dividir(int a, int b)
        {
            return a / b;
        }

        public int Multiplicar(int a, int b)
        {
            return a * b;
        }
    }
}